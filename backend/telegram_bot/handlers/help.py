from aiogram import Router
from aiogram.types import Message
from aiogram.filters import Command
from redis_client import redis_client as redis


router = Router()


@router.message(Command("help"))
async def handler_help(message: Message):
    telegram_id = message.from_user.id
    token = redis.get(f"token:{telegram_id}")
    if not token:
        await message.answer(f"–í—ã –Ω–µ –∞–≤—Ç–æ—Ä–∏–∑–æ–≤–∞–Ω—ã. –°–Ω–∞—á–∞–ª–∞ –≤—ã–ø–æ–ª–Ω–∏—Ç–µ /start")
        return
    text = f"–í–æ–∑–º–æ–∂–Ω–æ—Å—Ç–∏ –±–æ—Ç–∞ TrackIt ü§ñ\n1) /start - –≤—ã–ø–æ–ª–Ω–∏—Ç—å –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏—é (—Å–ø–µ—Ä–≤–∞ –≤—ã–æ–ª–Ω–∏—Ç–µ —ç—Ç—É –∫–æ–º–∞–Ω–¥—É, —á—Ç–æ–±—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç—å—Å—è –±–æ—Ç–æ–º);\n2) /help - –ø—Ä–æ—Å–º–æ—Ç—Ä –≤—Å–µ—Ö –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–µ–π –±–æ—Ç–∞ (—ç—Ç–æ —Å–æ–æ–±—â–µ–Ω–∏–µ - —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∫–æ–º–∞–Ω–¥—ã /help);\n3) /list - –ø—Ä–æ—Å–º–æ—Ç—Ä —Å–ø–∏—Å–∫–∞ –≤—Å–µ—Ö –æ—Ç—Å–ª–µ–∂–∏–≤–∞–µ–º—ã—Ö –í–∞–º–∏ –ø–æ—Å—ã–ª–æ–∫;\n4) /track <—Ç—Ä–µ–∫-–Ω–æ–º–µ—Ä> - –æ—Ç—Å–ª–µ–∂–∏–≤–∞—Ç—å —Å—Ç–∞—Ç—É—Å –ø–æ—Å—ã–ª–∫–∏ (—É–±–µ–¥–∏—Ç–µ—Å—å –≤ –≤–∞–ª–∏–¥–Ω–æ—Å—Ç–∏/–∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç–∏ —Ç—Ä–µ–∫-–Ω–æ–º–µ—Ä–∞ –ø–µ—Ä–µ–¥ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º –∫–æ–º–∞–Ω–¥—ã);\n5) /delete <—Ç—Ä–µ–∫-–Ω–æ–º–µ—Ä> - —É–¥–∞–ª–∏—Ç—å –ø–æ—Å—ã–ª–∫—É –∏–∑ —Å–ø–∏—Å–∫–∞ –æ—Ç—Å–ª–µ–∂–∏–≤–∞–µ–º—ã—Ö –ø–æ—Å—ã–ª–æ–∫."
    await message.answer(text)
